#[test]
fn file_attributes() {
    let default_file_attrs =
        EntryAttrs::READ | EntryAttrs::WRITE | EntryAttrs::EXECUTE | EntryAttrs::DELETE;
    let mut reader = ArchiveReader::new(Cursor::new(&FILE_ATTRIBUTES_LZX_ARCHIVE)).unwrap();
    while let Some(entry) = reader.next_entry().unwrap() {
        let a = entry.attrs;
        match entry.filename.as_str() {
            "d" => assert_eq!(a, EntryAttrs::DELETE),
            "e" => assert_eq!(a, EntryAttrs::EXECUTE),
            "w" => assert_eq!(a, EntryAttrs::WRITE),
            "r" => assert_eq!(a, EntryAttrs::READ),
            "a" => assert_eq!(a, EntryAttrs::ARCHIVE),
            "p" => assert_eq!(a, EntryAttrs::PURE),
            "s" => assert_eq!(a, EntryAttrs::SCRIPT),
            "h" => assert_eq!(a, EntryAttrs::HOLD),
            "default" => assert_eq!(a, default_file_attrs),
            _ => unreachable!(),
        }
    }
}
